package day3;

public class ExampleOne {
    public static void main(String[] args) {
        int x = 7;
        int y = 12;
        x = x + 3;
        x /= 2; // same as x = x / 2;
        y = y - 2;
        y %= 4; // divide y by 4 and store the remainder in y
        x++; // adds 1 to x
        y--;
    }
}
