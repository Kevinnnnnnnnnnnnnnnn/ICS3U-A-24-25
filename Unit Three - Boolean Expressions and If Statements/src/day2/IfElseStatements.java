package day2;

public class IfElseStatements {
    public static void main(String[] args) {
        int num = 65656;
        if (num % 2 == 0) System.out.println(num + " is even!"); // executed if the condition is true
        else System.out.println(num + " is odd!"); // executed if the condition is not true (false)
        // else statements cannot exist on their own       
    }
}
